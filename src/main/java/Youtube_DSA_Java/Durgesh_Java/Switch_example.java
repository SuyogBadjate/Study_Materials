package Youtube_DSA_Java.Durgesh_Java;

public class Switch_example {
    public static void main(String[] args) {
        String day="Mon";

        switch(day){
            case "Mon":
                System.out.println("It is Monday");
                break;

            case "Tues":
                System.out.println("It is Tuesday");
                break;

            default:
                System.out.println("Not valid day");

        }
    }
}
